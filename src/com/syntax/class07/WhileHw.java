package com.syntax.class07;

public class WhileHw {

	public static void main(String[] args) {
		// Print numbers from 1-100
		
		int i=1;
		
		while (i<=100) {
			System.out.print(i+ " ");
			i++;
		}
		
		System.out.println("-------------------------------");
		//Print numbers from 100-1
		
		int num=100;
		
		while(num>=1) {
			System.out.print(num+ " ");
			num--;
		}
	}
	
}
