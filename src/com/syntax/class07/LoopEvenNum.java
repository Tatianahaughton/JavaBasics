public class LoopEvenNum {

	public static void main(String[] args) {
		// print out only even numbers from 1-20
		
		int c=2;
		while(c<=20) {
			System.out.println(c + " ");
			c+=2;
		}
		
			System.out.println("-----------------------");
		
		int num=1;
		
		while(num<=20) {
			if(num%2==0) {
			System.out.println(num + " ");
			
		}
			num++;
		}
	}
	}


