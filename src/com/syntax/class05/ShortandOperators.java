package com.syntax.class05;

public class ShortandOperators {

	public static void main(String[] args) {
		int num=90;
		num=num+1;
		System.out.println(num);
		
		//another way to increment variable by 1
		
		num++;
		
		System.out.println(num);
		
		//do decrement
		
		num--;
		System.out.println(num);
		
		int i=10;
		i--;
		i--;
		System.out.println("i");
	}
}
		
	